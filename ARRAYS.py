# Declarando um array de strings com produtos de materiais de construção
materiais_construcao = ["Tijolo", "Cimento", "Tinta", "Tubo PVC", "Areia"]

# Imprimindo o array de materiais de construção
print("Materiais de construção:")
print(materiais_construcao)

# Imprimindo cada material em uma linha separada
for material in materiais_construcao:
    print(f"- {material}")